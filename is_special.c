#include"ft_printf.h"

int	is_special(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'o' ||
			c == 'u' || c == 'x' || c == 'X' || c == 'h' || c == 'l' ||
			c == 'f' || c == 'L' || c == '%' || c == '#' || c == '0' ||
			c == '-' || c == '+' || c == ' ' || ft_isdigit(c))
		return (1);
	else
		return (0);
}
